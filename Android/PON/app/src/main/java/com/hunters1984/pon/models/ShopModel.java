public class ShopModel {
    private String mShopLogo;
    private String mShopName;
    private String mNumberOfShopSubscribe;
    private boolean mIsShopSubscribe;

    public String getmShopLogo() {
        return mShopLogo;
    }

    public void setmShopLogo(String mShopLogo) {
        this.mShopLogo = mShopLogo;
    }

    public String getmShopName() {
        return mShopName;
    }

    public void setmShopName(String mShopName) {
        this.mShopName = mShopName;
    }

    public String getmNumberOfShopSubscribe() {
        return mNumberOfShopSubscribe;
    }

    public void setmNumberOfShopSubscribe(String mNumberOfShopSubscribe) {
        this.mNumberOfShopSubscribe = mNumberOfShopSubscribe;
    }

    public boolean ismIsShopSubscribe() {
        return mIsShopSubscribe;
    }

    public void setmIsShopSubscribe(boolean mIsShopSubscribe) {
        this.mIsShopSubscribe = mIsShopSubscribe;
    }
}
