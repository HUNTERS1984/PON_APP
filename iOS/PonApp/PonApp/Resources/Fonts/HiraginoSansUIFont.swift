import UIKit

extension UIFont {
    
    class func HiraginoSansW3(fontSize: CGFloat) -> UIFont {
        return UIFont(name: "HiraginoSansGB-W3", size: fontSize)!
    }
    
    class func HiraginoSansW6(fontSize: CGFloat) -> UIFont {
        return UIFont(name: "HiraginoSansGB-W6", size: fontSize)!
    }
    
}
